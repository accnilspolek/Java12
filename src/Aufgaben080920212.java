public class Aufgaben080920212 {
    //Taschenrechner
    static void subtrahieren(int zahl1,int zahl2){
        int ergebnis;
        ergebnis = zahl1 - zahl2;
        System.out.println(zahl1+" - "+zahl2+" = "+ergebnis);
    }
    public static void main(String[] args){
        subtrahieren(1, 2);
    }
}
